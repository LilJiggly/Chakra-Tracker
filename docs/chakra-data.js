const LEVELS = [
  { level: 1, name: 'Dormant',   min: 0,  max: 19  },
  { level: 2, name: 'Awakening', min: 20, max: 39  },
  { level: 3, name: 'Aligned',   min: 40, max: 59  },
  { level: 4, name: 'Flowing',   min: 60, max: 79  },
  { level: 5, name: 'Radiant',   min: 80, max: 100 },
];

function getLevel(progress) {
  return LEVELS.find(l => progress >= l.min && progress <= l.max) || LEVELS[0];
}

function getLevelProgress(progress) {
  const lvl = getLevel(progress);
  const range = lvl.max - lvl.min + 1;
  const within = progress - lvl.min;
  return Math.round((within / range) * 100);
}
